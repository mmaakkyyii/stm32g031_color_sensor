void print_uart(const char *format, ...){

	uint8_t buffer[128];
	va_list args;
	va_start(args,format);

	int n=vsprintf((char*)buffer,format,args);
	HAL_UART_Transmit(&hlpuart1, buffer, n,100);

	va_end(args);

}

uint8_t color_addr=0x2A<<1;

void init_S11059(){
	uint8_t data[2];
	data[0]=0x00;
	data[1]=0b10001001;

	if(HAL_I2C_Master_Transmit(&hi2c2, color_addr, &data, 2, 1000)==HAL_OK){
		print_uart("ok\r\n");
	}
	data[0]=0x00;
	data[1]=0b00001001;
	if(HAL_I2C_Master_Transmit(&hi2c2, color_addr, &data, 2, 1000)==HAL_OK){
		print_uart("ok\r\n");
	}
	HAL_Delay(2000);
}

void Updata_S11059(){
	uint8_t data_read[0]={0x03};
	uint8_t sensor[8]={0,0,0,0,0,0,0,0};

	HAL_I2C_Mem_Read(&hi2c2, color_addr|0b1, 0x03, 1, sensor, 8, 1000);

	int r =(((uint16_t)sensor[0])<<8 )| (uint16_t)sensor[1];
	int g =(((uint16_t)sensor[2])<<8 )| (uint16_t)sensor[3];
	int b =(((uint16_t)sensor[4])<<8 )| (uint16_t)sensor[5];
	int ir=(((uint16_t)sensor[6])<<8 )| (uint16_t)sensor[7];
	print_uart("r%5d,g%5d,b%5d,ir%5d\r\n",r,g,b,ir);
}
